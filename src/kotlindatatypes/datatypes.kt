package kotlindatatypes

fun main(){
    val myByte: Byte = 100
    println(myByte)

    val myShort: Short = 5000
    println(myShort)

    val myInt: Int = 100000
    println(myInt)

    val myLong: Long = 15000000000L
    println(myLong)

    val myFloat: Float = 5.75F
    println(myFloat)

    val myDouble: Double = 19.99
    println(myDouble)

    val myScientificFloat: Float = 35E3F
    println(myScientificFloat)
    val myScientificDouble: Double = 12E4
    println(myScientificDouble)

    val isKotlinFun: Boolean = true
    val isFishTasty: Boolean = false
    println(isKotlinFun)   // Outputs true
    println(isFishTasty)   // Outputs false

    val myGrade: Char = 'B'
    println(myGrade)

    val myText: String = "Hello World"
    println(myText)
}
